# if
a = 5
b = 10
if a>b :
    print("a more than b")

# if .. else
a = 5
b = 10
if a>b :
    print("a more than b")
else:
    print("b more than a")
print("out of if")

# if ...elif .. else
a = 10
b = 10
if a>b :
    print("a more than b")
elif a<b:
    print("b more than a")
else:
    print("a and b are equal")
    
print("out of if")